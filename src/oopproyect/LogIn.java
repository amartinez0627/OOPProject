    private void logInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInBtnActionPerformed
        ResultSet rs;
        String selectedUser =jComboBox1.getSelectedItem().toString();
        int user= 0;
        String pass= null;
        if(selectedUser.equals("Estudiante")){
            rs = crud.readStudentData(Integer.parseInt(userTxtFld.getText()));
            
            try{
                while(rs.next()){
                user = rs.getInt("id");
                pass = rs.getString("pass");
                }
            }catch(NumberFormatException | SQLException e){
                System.out.println(e);
            }
            if(user == Integer.parseInt(userTxtFld.getText()) && 
                        pass.equals(new String(passTxtFld.getPassword()))){
                    Menu menu = new Menu(1);
                }
        }
        
        
        else if(selectedUser.equals("Maestro")){
            rs = crud.readTeacherData(Integer.parseInt(userTxtFld.getText()));
            
            try{
               while(rs.next()){
               user = rs.getInt("id");
               pass = rs.getString("pass");
               }
            }catch(NumberFormatException | SQLException e){
                System.out.println(e);
            }
            
             if(user == Integer.parseInt(userTxtFld.getText()) && 
                        pass.equals(new String(passTxtFld.getPassword()))){
                    Menu menu = new Menu(2);
                }
            
        }
        
        else if(selectedUser.equals("Administrador")){
            rs = crud.readAdminData(Integer.parseInt(userTxtFld.getText()));
            try{
                while(rs.next()){
                user = rs.getInt("id");
               pass = rs.getString("pass");    
                }
            }catch(NumberFormatException | SQLException e){
                System.out.println(e);
            }
            
            if(user == Integer.parseInt(userTxtFld.getText()) && 
                        pass.equals(new String(passTxtFld.getPassword()))){
                    Menu menu = new Menu(3);
                }
                    
        }
        
    }//GEN-LAST:event_logInBtnActionPerformed

